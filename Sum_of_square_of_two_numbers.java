import java.util.Scanner;
public class Sum_of_square_of_two_numbers {
  public static void main(String[] args)
  {
    Scanner sc=new Scanner(System.in);
    int g=sc.nextInt();
    int h=sc.nextInt();
    
    int Sum_of_square_of_two_numbers = ((g*g)+(h*h));
    System.out.println("Sum_of_square_of_two_numbers = "+Sum_of_square_of_two_numbers);
  }
}